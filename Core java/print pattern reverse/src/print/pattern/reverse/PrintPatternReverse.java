
package print.pattern.reverse;


public class PrintPatternReverse {

    
    public static void main(String[] args) 
    
    {
        int a,b;
         for( a=1;a<=7;a++)
         {
             for(b=6;b>=a;b--)
             {
                 System.out.print("*");
             }
             System.out.println("");
         }
         }
    }
    

