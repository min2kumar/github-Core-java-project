package model;

public class Info {

    int id;
    String name;
    String dept;
    int salary;
    
    
    
    
    public int getId() {
        return id;
    }




    public void setId(int id) {
        this.id = id;
    }




    public String getName() {
        return name;
    }




    public void setName(String name) {
        this.name = name;
    }




    public String getDept() {
        return dept;
    }




    public void setDept(String dept) {
        this.dept = dept;
    }




    public int getSalary() {
        return salary;
    }




    public void setSalary(int salary) {
        this.salary = salary;
    }




    public Info() 
    {
	
    }

}
